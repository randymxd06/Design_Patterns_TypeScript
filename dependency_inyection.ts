/*===========================================================================================================

Dependency Injection:

Propósito: Permite la inyección de dependencias en un objeto, en lugar de que el objeto cree sus dependencias directamente.

Implementación: Las dependencias se pasan al objeto desde el exterior, generalmente a través de la inyección de constructor, métodos o propiedades.

Ejemplo de uso: Útil para desacoplar componentes y facilitar la prueba unitaria, ya que las dependencias pueden ser reemplazadas por versiones simuladas o de prueba.

===========================================================================================================*/
